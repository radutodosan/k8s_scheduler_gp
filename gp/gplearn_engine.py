"""GplearnEngine — gplearn-based implementation of IGeneticEngine.

Uses gplearn's SymbolicRegressor for symbolic regression on scheduling
decision data, producing interpretable expression trees comparable to
DEAP-evolved rules.

Unlike DEAP (simulation-based fitness), this engine trains on labeled
scheduling data generated by running a reference strategy on the
training instances.  The terminal features for each (pod, node) pair
are the X inputs, and the target y is a reference quality score.

This creates a meaningful comparison for the dissertation:
  - DEAP: **online** learning — rules optimised directly via simulation
  - gplearn: **offline** learning — rules learned from reference schedules
"""

